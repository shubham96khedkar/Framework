package javaProgramsNotepad3;

public class SumOfElementsInArray {

	public static void main(String[] args) {
		
		int[] a={1,2,3,4,5};

		int sum=0;

		for(int i=0;i<a.length;i++){

		sum=sum+a[i];   //i=0  sum=1
	                   //i=1  sum=3}
		}
		System.out.println("sum of all elements in array= "+sum);

		
	}

}
